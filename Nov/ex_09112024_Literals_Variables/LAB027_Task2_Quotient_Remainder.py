#Take two number and find the Quotient and Remainder

a = int(input("Enter value of a: "))
b = int(input("Enter value of b: "))

Quotient = a // b
Remainder = a % b
print("Quotient of a and b is ", Quotient)
print("Remainder of a and b is ", int(Remainder))


#Dynamicaly Typed
# At the run time, data of the variable can be changed and you don't need to
#mention the data type in python

age = 30
AGE = 95
print(type(age))
print(type(AGE))
#Both Age are different because python is case-sensitive

age = "Dynamically_typed_example"
print(type(age))

age = True
print(type(age))

